const mergeContiguousGroups = (columnLetter, rowIndex) => {
  let start = 3
  let current = rows[0]?.[rowIndex] ?? ''

  for (let index = 1; index <= rows.length; index += 1) {
    const next = rows[index]?.[rowIndex] ?? ''
    if (next === current) continue

    const end = index + 2
    if (end > start) sheet.getRange(`${columnLetter}${start}:${columnLetter}${end}`).merge()
    start = index + 3
    current = next
  }
}

