def style_sheet(ws) -> None:
    thin = Side(style="thin", color="D9E2F3")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)
    header_fill = PatternFill("solid", fgColor="1F4E79")
    module_fill = PatternFill("solid", fgColor="EAF2F8")
    first_fill = PatternFill("solid", fgColor="F6FAFD")

    ws.freeze_panes = "A2"
    ws.auto_filter.ref = f"A1:D{ws.max_row}"

    for cell in ws[1]:
        cell.font = Font(name="Microsoft YaHei", color="FFFFFF", bold=True, size=11)
        cell.fill = header_fill
        cell.alignment = Alignment(horizontal="center", vertical="center")
        cell.border = border

    for row in ws.iter_rows(min_row=2, max_row=ws.max_row):
        for cell in row:
            cell.font = Font(name="Microsoft YaHei", size=10)
            cell.alignment = Alignment(vertical="top", wrap_text=True)
            cell.border = border
        row[0].fill = module_fill
        row[1].fill = first_fill

    widths = [24, 22, 24, 82]
    for idx, width in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(idx)].width = width

    for row_idx in range(2, ws.max_row + 1):
        ws.row_dimensions[row_idx].height = 42
    ws.row_dimensions[1].height = 28


def merge_repeated(ws, col_idx: int) -> None:
    start = 2
    current = ws.cell(row=start, column=col_idx).value
    for row in range(3, ws.max_row + 2):
        value = ws.cell(row=row, column=col_idx).value if row <= ws.max_row else None
        if value != current:
            if row - start > 1:
                ws.merge_cells(start_row=start, start_column=col_idx, end_row=row - 1, end_column=col_idx)
                ws.cell(row=start, column=col_idx).alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
            start = row
            current = value


def build_workbook() -> Path:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    wb = Workbook()
    ws = wb.active
    ws.title = "功能清单"
    ws.append(HEADERS)
    for row in ROWS:
        ws.append(row)

    style_sheet(ws)
    merge_repeated(ws, 1)
    merge_repeated(ws, 2)

    info = wb.create_sheet("说明")
    info_rows = [
        ("文档用途", "一期岗位中心功能清单，供产品、研发、测试和实施拆分工作项使用。"),
        ("范围", "覆盖岗位数据调研-产业布局、岗位数据调研-岗位分析、岗位建设中心、查看成果页-岗位中心。"),
        ("字段说明", "模块为一期交付模块；一级功能为页面或功能组；二级功能为可拆分工作项；功能描述为交付说明。"),
        ("维护建议", "后续新增功能直接按模块、一级功能、二级功能追加，避免写入技术实现细节。"),
    ]
    info.append(["项目", "说明"])
    for row in info_rows:
        info.append(row)
    for cell in info[1]:
        cell.font = Font(name="Microsoft YaHei", color="FFFFFF", bold=True)
        cell.fill = PatternFill("solid", fgColor="1F4E79")
        cell.alignment = Alignment(horizontal="center", vertical="center")
    for row in info.iter_rows(min_row=1, max_row=info.max_row, max_col=2):
        for cell in row:
            cell.border = Border(
                left=Side(style="thin", color="D9E2F3"),
                right=Side(style="thin", color="D9E2F3"),
                top=Side(style="thin", color="D9E2F3"),
                bottom=Side(style="thin", color="D9E2F3"),
            )
            cell.alignment = Alignment(vertical="top", wrap_text=True)
            cell.font = Font(name="Microsoft YaHei", size=10, bold=cell.row == 1)
    info.column_dimensions["A"].width = 18
    info.column_dimensions["B"].width = 78

    wb.save(OUT_PATH)
    return OUT_PATH


